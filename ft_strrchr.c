#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i1;

	i1 = ft_strlen(s);
	while (i1 >= 0)
	{
		if (c % 256 >= 1)
			c -= (256 * (c / 256));
		if (s[i1] == c)
			return ((char *)&s[i1]);
		i1--;
	}
	return (0);
}
