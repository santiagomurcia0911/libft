#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i1;

	i1 = 0;
	while (i1 < len)
	{
		((unsigned char *)b)[i1] = c;
		i1++;
	}
	return (b);
}
